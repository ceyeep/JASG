package edu.utep.cs.jasg.fileGenerator;
/** Generates a new AST from given user nodes */
public class ASTGenerator {
	
	private StringBuffer ASTTemplate = new StringBuffer();
	
	
	/** Generates AST template */
	public String generateAST()
	{
		ASTTemplate.append(""); 
		
		return ASTTemplate.toString();
	}
}
